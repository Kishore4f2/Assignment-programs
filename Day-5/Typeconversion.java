  public class Typeconversion 
{
    public static void main(String[] args)
 {
        int num1 = 25;
        double num2 = num1; // int to double
        System.out.println("Int to Double: " + num2);

        double num3 = 45.78;
        int num4 = (int) num3; // double to int
        System.out.println("Double to Int: " + num4);
 }
}

